class MagicSpacing {
  /// 4px
  final double xs;

  /// 8px
  final double sm;

  /// 16px
  final double md;

  /// 24px
  final double lg;

  /// 32px
  final double xl;

  /// 48px
  final double xxl;

  const MagicSpacing({
    this.xs = 4,
    this.sm = 8,
    this.md = 16,
    this.lg = 24,
    this.xl = 32,
    this.xxl = 48,
  });
}
