  // sl.registerFactory(() => ${pascal}RepositoryImpl(datasource: sl()));
  // sl.registerFactory(() => ${pascal}Usecase(repository: sl()));

  sl.registerFactory(() => ${pascal}Cubit());
}
''';

  String _diWithBlocTemplate(String pascal, String snake, String camel) => '''
// GENERATED BY MAGICKIT CLI

import 'package:get_it/get_it.dart';
import '../presentation/cubit/${snake}_cubit.dart';
import '../presentation/bloc/${snake}_bloc.dart';

