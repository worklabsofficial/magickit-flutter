  // TODO: Register datasource, repository, usecase saat sudah diisi
  // sl.registerFactory(() => ${pascal}RemoteDatasourceImpl(client: sl()));
  // sl.registerFactory(() => ${pascal}RepositoryImpl(datasource: sl()));
  // sl.registerFactory(() => Fetch${pascal}Usecase(repository: sl()));

  sl.registerFactory(() => ${pascal}Bloc());
  sl.registerFactory(() => ${pascal}Cubit(${camel}Bloc: sl()));
}
''';

  String _blocTemplate(String pascal, String snake) => '''
// GENERATED BY MAGICKIT CLI

import 'package:flutter_bloc/flutter_bloc.dart';
import '${snake}_event.dart';
import '${snake}_state.dart';

class ${pascal}Bloc extends Bloc<${pascal}Event, ${pascal}State> {
  ${pascal}Bloc() : super(const ${pascal}Initial()) {
    on<Fetch${pascal}>(_onFetch);
  }

  Future<void> _onFetch(
    Fetch${pascal} event,
    Emitter<${pascal}State> emit,
  ) async {
    emit(const ${pascal}Loading());
    try {
      // TODO: call usecase
      emit(const ${pascal}Success());
    } catch (e) {
      emit(${pascal}Failed(e));
    }
  }
}
''';

  String _blocEventTemplate(String pascal) => '''
// GENERATED BY MAGICKIT CLI

abstract class ${pascal}Event {
  const ${pascal}Event();
}

class Fetch${pascal} extends ${pascal}Event {
  const Fetch${pascal}();
}
''';

  String _blocStateTemplate(String pascal) => '''
// GENERATED BY MAGICKIT CLI

abstract class ${pascal}State {
  const ${pascal}State();
}

class ${pascal}Initial extends ${pascal}State {
  const ${pascal}Initial();
}

class ${pascal}Loading extends ${pascal}State {
  const ${pascal}Loading();
}

class ${pascal}Success extends ${pascal}State {
  const ${pascal}Success();
}

class ${pascal}Failed extends ${pascal}State {
  final Object failure;
  const ${pascal}Failed(this.failure);
}
''';

  // ─── Helpers ────────────────────────────────────────────────────────────────

  String _toCamel(String snake) {
    final parts = snake.split('_');
    if (parts.isEmpty) return snake;
    return parts.first +
        parts.skip(1).map((p) => p[0].toUpperCase() + p.substring(1)).join('');
  }
}
